# ESTE CÓDIGO REALIZA LAS OPERERACIONES BASICAS (SUMA, RESTA, MULTIPLICACION Y DIVISION) DE DOS TERMINOS CUYO VALOR ES ASIGNADO EN EL MISMO CÓDIGO.

# 2025 / 03/ 03 - V. 1. 0. 0 - OPERACIONES BASICAS


numero1 = 5 # Se asigna el numero 5 a la variable "numero1"
numero2 = 3 # Se asigna el valor de 3 a la variable "numero2"


suma = numero1 + numero2     # Suma de terminos almacaenados en la variable numero1 y numero2
resta = numero1 - numero2    # Resta de terminos almacaenados en la variable numero1 y numero2
multiplicacion = numero1 * numero2 # Multiplica los valores almacenados en numero1 y numero2, y el resultado se guarda en la variable multiplicacion.
division = numero1 / numero2 #Divide el valor de numero1 entre numero2, y el resultado se almacena en la variable division.


print("Suma:", suma) # Muestra en pantalla el texto "Suma:" seguido del valor almacenado en la variable suma.
print("Resta:", resta) #Muestra "Resta:" seguido del valor de la variable resta.
print("Multiplicación:", multiplicacion) #Muestra "Multiplicación:" seguido del valor de multiplicacion
print("División:", division) #Muestra "División:" seguido del valor de division

