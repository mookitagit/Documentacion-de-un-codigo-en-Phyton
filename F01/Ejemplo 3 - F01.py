# ESTE CÓDIGO IDENTIFICA SI UN NUMERO DADO POR EL USUARIO ES PAR O IMPAR.

# 2025 / 03/ 03 - V. 1. 0. 0 - PARES E IMPARES

numero = int(input("Ingrese un número: "))  # Se agrega una entrada de tipo entero dada por el usuario.

if numero % 2 == 0:
    print("El número es par") # si la condicion if anterior se cumple numero es divisible entre 2 y se imprime "El número es par".
else:
    print("El número es impar")
